namespace SixLabors.Fonts
{
    /// <summary>
    /// Vertial alignment modes.
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// Aligns downward from the top.
        /// </summary>
        Top = 0,

        /// <summary>
        /// Aligns text up and down from the middle.
        /// </summary>
        Center = 1,

        /// <summary>
        /// Aligns text upwards from the bottom
        /// </summary>
        Bottom = 2
    }
}